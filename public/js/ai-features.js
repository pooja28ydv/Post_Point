// ─── 6. Semantic Search ───────────────────────────────────────────────────────

window.runSmartSearch = async function (e) {
    if (e) e.preventDefault();
    const input   = document.getElementById("smart-search-input");
    const results = document.getElementById("smart-search-results");
    const query   = input ? input.value.trim() : "";

    if (!query) { input && input.focus(); return; }

    results.innerHTML = `
        <div class="ai-loader" style="padding:1rem">
            <div class="ai-spinner" style="width:32px;height:32px;border-width:3px"></div>
            <p style="font-size:0.8rem">Searching by meaning...</p>
        </div>`;
    results.style.display = "block";

    try {
        const resp = await fetch("/api/ai/semantic-search", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({ query })
        });
        const data = await resp.json();
        if (!resp.ok) throw new Error(data.error);

        if (!data.results || data.results.length === 0) {
            results.innerHTML = `<div class="ai-empty">🔍 No blogs found matching "${query}"</div>`;
            return;
        }

        const cards = data.results.map(b => `
            <a href="/listings/${b._id}" class="listing-link">
                <div class="card listing-card" style="width:100%;margin-bottom:0.75rem">
                    <div style="display:flex;align-items:center;gap:1rem;padding:0.75rem">
                        <img src="${b.image?.url || ''}" style="width:70px;height:70px;object-fit:cover;border-radius:8px;flex-shrink:0" alt="">
                        <div>
                            <div style="font-weight:700;font-size:0.9rem;color:#1f2937">${b.title}</div>
                            <div style="font-size:0.75rem;color:#6c63ff;font-weight:600;text-transform:uppercase;letter-spacing:0.04em">${b.category}</div>
                            <div style="font-size:0.75rem;color:#888;margin-top:0.2rem">⭐ ${b.averageRating?.toFixed(1) || '0.0'}</div>
                        </div>
                        <span class="smart-search-badge" style="margin-left:auto;font-size:0.65rem">AI Match</span>
                    </div>
                </div>
            </a>`).join("");

        results.innerHTML = `
            <div class="smart-search-badge" style="margin-bottom:0.75rem">
                🔍 ${data.count} AI result${data.count !== 1 ? "s" : ""} for "${query}"
            </div>
            ${cards}
            <button onclick="clearSmartSearch()" class="btn-ai btn-ai-outline" style="margin-top:0.5rem;width:100%">
                ✕ Clear & show all blogs
            </button>`;
    } catch (err) {
        results.innerHTML = `<div class="ai-error"><span>⚠️</span><span>${err.message}</span></div>`;
    }
};

window.clearSmartSearch = function () {
    const input   = document.getElementById("smart-search-input");
    const results = document.getElementById("smart-search-results");
    if (input) input.value = "";
    if (results) { results.innerHTML = ""; results.style.display = "none"; }
};

// ─── Recommendations loader ───────────────────────────────────────────────────
window.loadRecommendations = async function (blogId) {
    const panel = document.getElementById("recommendations-panel");
    if (!panel) return;

    try {
        const resp = await fetch(`/api/ai/recommendations/${blogId}`);
        const data = await resp.json();
        if (!resp.ok || !data.recommendations || data.recommendations.length === 0) {
            panel.innerHTML = `<div class="ai-empty">No recommendations yet. Be the first to publish more blogs! 🚀</div>`;
            return;
        }

        panel.innerHTML = `
            <div class="recommendations-grid">
                ${data.recommendations.map(b => `
                    <a href="/listings/${b._id}" class="rec-card">
                        <img src="${b.image?.url || ''}" alt="${b.title}" onerror="this.src='https://via.placeholder.com/200x120?text=Blog'">
                        <div class="rec-card-body">
                            <div class="rec-card-title">${b.title}</div>
                            <div class="rec-card-cat">${b.category}</div>
                        </div>
                    </a>`).join("")}
            </div>`;
    } catch (err) {
        panel.innerHTML = `<div class="ai-error"><span>⚠️</span><span>Could not load recommendations</span></div>`;
    }
};
