// ─── Utility helpers ──────────────────────────────────────────────────────────

function showLoader(containerId, message = "AI is thinking...") {
    const el = document.getElementById(containerId);
    if (!el) return;
    el.innerHTML = `
        <div class="ai-loader">
            <div class="ai-spinner"></div>
            <p>✨ ${message}</p>
        </div>`;
    el.classList.remove("hidden");
}

function showError(containerId, message) {
    const el = document.getElementById(containerId);
    if (!el) return;
    el.innerHTML = `
        <div class="ai-error">
            <span style="font-size:1.4rem">⚠️</span>
            <span>${message}</span>
        </div>`;
    el.classList.remove("hidden");
}

async function apiFetch(url, body) {
    const resp = await fetch(url, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(body)
    });
    const data = await resp.json();
    if (!resp.ok) throw new Error(data.error || "Request failed");
    return data;
}

function scoreColor(val) {
    if (val >= 7.5) return "#10b981";
    if (val >= 5)   return "#f59e0b";
    return "#ef4444";
}

function scoreClass(val) {
    if (val >= 7.5) return "score-high";
    if (val >= 5)   return "score-medium";
    return "score-low";
}

// ─── 1. AI Image Generator ────────────────────────────────────────────────────

window.generateAIImage = async function () {
    const titleInput = document.querySelector('input[name="listing[title]"]');
    const title      = titleInput ? titleInput.value.trim() : "";
    const styleEl    = document.getElementById("ai-image-style");
    const style      = styleEl ? styleEl.value : "realistic";
    const panel      = document.getElementById("ai-image-panel");
    const preview    = document.getElementById("ai-image-preview");
    const imgEl      = document.getElementById("ai-generated-img");
    const useBtn     = document.getElementById("use-ai-image-btn");
    const btn        = document.getElementById("generate-img-btn");
    const urlInput   = document.getElementById("ai-image-url-hidden");
    const sourceEl   = document.getElementById("ai-image-source");

    if (!title) {
        alert("Please enter a blog title first!");
        titleInput && titleInput.focus();
        return;
    }

    btn.disabled = true;
    btn.innerHTML = `<span class="ai-spinner" style="width:16px;height:16px;border-width:2px;display:inline-block;vertical-align:middle;margin-right:6px"></span> Generating...`;

    try {
        const resp = await fetch("/api/ai/generate-image", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({ title, style })
        });
        const data = await resp.json();
        if (!resp.ok) throw new Error(data.error);

        // Show image — works for both base64 data URIs and external URLs
        imgEl.src = data.imageUrl;

        const showPanel = () => {
            if (preview) preview.style.display = "block";
            if (panel)   panel.style.display   = "block";

            // Show source badge
            if (sourceEl) {
                const isGemini = data.isBase64;
                sourceEl.innerHTML = isGemini
                    ? `<span style="background:linear-gradient(135deg,#6c63ff,#a78bfa);color:white;padding:0.2rem 0.65rem;border-radius:50px;font-size:0.7rem;font-weight:700">✨ Generated by Gemini AI</span>`
                    : `<span style="background:linear-gradient(135deg,#10b981,#34d399);color:white;padding:0.2rem 0.65rem;border-radius:50px;font-size:0.7rem;font-weight:700">🎨 Generated by Pollinations.ai</span>`;
            }
        };

        imgEl.onload  = showPanel;
        imgEl.onerror = showPanel; // Still show panel even if img fails

        if (urlInput) urlInput.value = data.imageUrl;

        useBtn.onclick = () => {
            // ── Mark AI image as selected: hide upload requirement ────────────
            const fileInput     = document.getElementById("manual-image-input");
            const optionalBadge = document.getElementById("upload-optional-badge");
            const selectedBanner= document.getElementById("ai-selected-banner");
            const orDivider     = document.getElementById("or-divider-text");
            const uploadHint    = document.getElementById("upload-hint");

            if (fileInput)      fileInput.removeAttribute("required");
            if (optionalBadge)  optionalBadge.style.display = "inline";
            if (selectedBanner) selectedBanner.style.display = "block";
            if (orDivider)      orDivider.textContent = "— OR also upload a custom image —";
            if (uploadHint)     uploadHint.textContent = "✅ AI image selected — upload only if you want to override it.";

            // Confirm in button
            useBtn.innerHTML = "✅ AI Image Selected!";
            useBtn.style.background = "linear-gradient(135deg,#10b981,#34d399)";
            setTimeout(() => {
                useBtn.innerHTML = "✅ Use This Image (Skip Upload)";
                useBtn.style.background = "";
            }, 2000);
        };


    } catch (err) {
        if (panel) {
            panel.innerHTML = `<div class="ai-error"><span>⚠️</span><span>Generation failed: ${err.message}</span></div>`;
            panel.style.display = "block";
        }
    } finally {
        btn.disabled = false;
        btn.innerHTML = `✨ Generate AI Image`;
    }
};

window.regenerateImage = function () {
    generateAIImage();
};


// ─── 2. Quality Score ─────────────────────────────────────────────────────────

window.analyzeQuality = async function () {
    const title   = document.getElementById("blog-title-data")?.dataset.title || "";
    const content = document.getElementById("blog-content-data")?.dataset.content || "";
    const panel   = document.getElementById("quality-panel");

    showLoader("quality-panel", "Analyzing content quality...");

    try {
        const data = await apiFetch("/api/ai/quality-score", { title, content });
        renderQualityScore(data);
    } catch (err) {
        showError("quality-panel", err.message);
    }
};

function renderQualityScore(d) {
    const panel = document.getElementById("quality-panel");
    const metrics = [
        { label: "Readability", key: "readability" },
        { label: "SEO Score",   key: "seo_score" },
        { label: "Engagement",  key: "engagement" },
        { label: "Structure",   key: "structure" }
    ];

    const bars = metrics.map(m => {
        const val = d[m.key] || 0;
        const pct = (val / 10) * 100;
        return `
            <div class="quality-bar-wrap">
                <div class="quality-bar-label">
                    <span>${m.label}</span>
                    <span style="color:${scoreColor(val)};font-weight:700">${val.toFixed(1)}</span>
                </div>
                <div class="quality-bar">
                    <div class="quality-bar-fill" style="width:0%" data-target="${pct}"></div>
                </div>
            </div>`;
    }).join("");

    const overallVal = d.overall || 0;
    const improvItems = (d.improvements || []).map(i =>
        `<div class="improvement-item">${i}</div>`
    ).join("");

    panel.innerHTML = `
        <div style="display:flex;align-items:center;gap:1.5rem;margin-bottom:1.25rem;flex-wrap:wrap">
            <div style="text-align:center">
                <div class="score-ring ${scoreClass(overallVal)}" style="width:80px;height:80px;display:flex;align-items:center;justify-content:center;border-radius:50%;border:6px solid ${scoreColor(overallVal)};font-size:1.4rem;font-weight:800;color:${scoreColor(overallVal)}">
                    ${overallVal.toFixed(1)}
                </div>
                <div style="font-size:0.7rem;color:#888;margin-top:0.3rem;font-weight:600;text-transform:uppercase">Overall</div>
            </div>
            <div style="flex:1;min-width:200px">${bars}</div>
        </div>
        <div class="ai-summary-box">💡 ${d.summary}</div>
        ${improvItems ? `<div class="quality-improvements"><h6>💡 Improvements</h6>${improvItems}</div>` : ""}`;

    // Animate bars
    setTimeout(() => {
        panel.querySelectorAll(".quality-bar-fill").forEach(bar => {
            bar.style.width = bar.dataset.target + "%";
        });
    }, 100);
}

// ─── 3. Auto Translation ──────────────────────────────────────────────────────

window.translateBlog = async function (lang, flag) {
    const title   = document.getElementById("blog-title-data")?.dataset.title || "";
    const content = document.getElementById("blog-content-data")?.dataset.content || "";
    const panel   = document.getElementById("translation-panel");

    // Update active tab
    document.querySelectorAll(".lang-tab").forEach(t => t.classList.remove("active"));
    
    // Safely retrieve event
    const evt = window.event;
    if (evt && evt.currentTarget) {
        evt.currentTarget.classList.add("active");
    } else {
        // Fallback: find the clicked button containing the language code
        const tabs = document.querySelectorAll(".lang-tab");
        for (const tab of tabs) {
            if (tab.getAttribute("onclick") && tab.getAttribute("onclick").includes(lang)) {
                tab.classList.add("active");
                break;
            }
        }
    }

    showLoader("translation-panel", `Translating to ${lang}...`);

    try {
        const data = await apiFetch("/api/ai/translate", {
            title, content, language: lang
        });
        panel.innerHTML = `
            <div class="translation-title">${flag} ${data.title}</div>
            <div class="translation-content">${data.content}</div>`;
        panel.classList.remove("hidden");
    } catch (err) {
        showError("translation-panel", err.message);
    }
};

window.showOriginal = function () {
    const panel = document.getElementById("translation-panel");
    document.querySelectorAll(".lang-tab").forEach(t => t.classList.remove("active"));
    
    const evt = window.event;
    if (evt && evt.currentTarget) {
        evt.currentTarget.classList.add("active");
    } else {
        // Fallback: make the first tab (original English) active
        const originalTab = document.querySelector(".lang-tab");
        if (originalTab) {
            originalTab.classList.add("active");
        }
    }
    panel.classList.add("hidden");
};

// ─── 4. Plagiarism Check ──────────────────────────────────────────────────────

window.checkPlagiarism = async function () {
    const title   = document.getElementById("blog-title-data")?.dataset.title || "";
    const content = document.getElementById("blog-content-data")?.dataset.content || "";
    const panel   = document.getElementById("plagiarism-panel");

    showLoader("plagiarism-panel", "Checking originality...");

    try {
        const d = await apiFetch("/api/ai/plagiarism-check", { title, content });
        renderPlagiarism(d);
    } catch (err) {
        showError("plagiarism-panel", err.message);
    }
};

function renderPlagiarism(d) {
    const panel      = document.getElementById("plagiarism-panel");
    const score      = d.originality_score || 0;
    const radius     = 36;
    const circ       = 2 * Math.PI * radius;
    const offset     = circ * (1 - score / 100);

    const verdictClass = {
        "Original":        "verdict-original",
        "Mostly Original": "verdict-mostly",
        "Partially Similar": "verdict-partial",
        "Highly Similar":  "verdict-similar"
    }[d.verdict] || "verdict-original";

    const riskColors = { Low: "#10b981", Medium: "#f59e0b", High: "#ef4444" };
    const riskColor  = riskColors[d.risk_level] || "#10b981";
    const strokeColor = score >= 80 ? "#10b981" : score >= 60 ? "#f59e0b" : "#ef4444";

    const similarList = (d.similar_blogs || []).length > 0
        ? `<div style="margin-top:0.75rem;font-size:0.82rem;color:#555">
            <strong style="color:#6c63ff">Similar blogs found:</strong><br>
            ${d.similar_blogs.map(s => `• ${s}`).join("<br>")}
           </div>`
        : "";

    panel.innerHTML = `
        <div class="originality-header">
            <div class="originality-circle">
                <svg width="90" height="90" viewBox="0 0 90 90">
                    <circle class="circle-bg" cx="45" cy="45" r="${radius}"/>
                    <circle class="circle-fill" cx="45" cy="45" r="${radius}"
                        stroke="${strokeColor}"
                        stroke-dasharray="${circ}"
                        stroke-dashoffset="${circ}"
                        id="orig-circle"/>
                </svg>
                <div class="originality-percent">
                    <strong id="orig-num">0</strong>%
                    <span>original</span>
                </div>
            </div>
            <div class="originality-info">
                <div class="verdict-badge ${verdictClass}">
                    ${d.verdict === "Original" || d.verdict === "Mostly Original" ? "✅" : "⚠️"} ${d.verdict}
                </div>
                <div style="font-size:0.82rem;margin-bottom:0.35rem">
                    Risk Level: <strong style="color:${riskColor}">${d.risk_level}</strong>
                </div>
                <div style="font-size:0.85rem;color:#555;line-height:1.5">${d.feedback}</div>
                ${similarList}
            </div>
        </div>`;

    // Animate circle
    setTimeout(() => {
        const circle  = document.getElementById("orig-circle");
        const numEl   = document.getElementById("orig-num");
        if (circle) circle.style.strokeDashoffset = offset;
        if (numEl) {
            let cur = 0;
            const step = score / 40;
            const interval = setInterval(() => {
                cur = Math.min(cur + step, score);
                numEl.textContent = Math.round(cur);
                if (cur >= score) clearInterval(interval);
            }, 25);
        }
    }, 100);
}

